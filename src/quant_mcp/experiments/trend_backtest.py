"""Run a Kraken trend-strategy research pass through the MCP tool surface.

The runner is intentionally orchestration-only: it calls the same MCP tools a
Codex session would use and keeps live trading out of scope.
"""

from __future__ import annotations

import argparse
import asyncio
import json
import os
import sys
from dataclasses import asdict, dataclass
from datetime import UTC, datetime
from pathlib import Path
from typing import Any

from fastmcp import Client


DEFAULT_LOOKBACKS = ("5:20", "10:30", "20:50")
DEFAULT_SYMBOLS = ("BTC/USD", "SOL/USD")


@dataclass(frozen=True)
class LookbackPair:
    fast: int
    slow: int


@dataclass(frozen=True)
class ExperimentSummary:
    rank: int
    symbol: str
    dataset_id: str
    strategy_id: str
    lookback_fast: int
    lookback_slow: int
    backtest_status: str
    walk_forward_status: str
    total_return_pct: float
    benchmark_return_pct: float
    max_drawdown_pct: float
    trades: int
    walk_forward_total_return_pct: float
    positive_fold_rate_pct: float
    score: float


def repo_root_from_module() -> Path:
    return Path(__file__).resolve().parents[3]


def dataset_id_for(symbol: str, interval_minutes: int) -> str:
    return f"{symbol.replace('/', '_').lower()}_{interval_minutes}m"


def parse_lookback(value: str) -> LookbackPair:
    try:
        fast_raw, slow_raw = value.split(":", maxsplit=1)
        fast = int(fast_raw)
        slow = int(slow_raw)
    except ValueError as exc:
        raise argparse.ArgumentTypeError("lookbacks must use FAST:SLOW, for example 10:30") from exc

    if fast <= 0 or slow <= 0:
        raise argparse.ArgumentTypeError("lookback values must be positive integers")
    if fast >= slow:
        raise argparse.ArgumentTypeError("fast lookback must be smaller than slow lookback")
    return LookbackPair(fast=fast, slow=slow)


def mcp_stdio_config(repo_root: Path, python_exe: str) -> dict[str, Any]:
    src_path = str(repo_root / "src")
    existing_pythonpath = os.environ.get("PYTHONPATH")
    pythonpath = src_path if not existing_pythonpath else os.pathsep.join([src_path, existing_pythonpath])
    return {
        "mcpServers": {
            "QuantResearchMCP": {
                "command": python_exe,
                "args": ["-m", "quant_mcp.main"],
                "cwd": str(repo_root),
                "env": {
                    "MCP_TRANSPORT": "stdio",
                    "PYTHONPATH": pythonpath,
                    "PYTHONDONTWRITEBYTECODE": os.environ.get("PYTHONDONTWRITEBYTECODE", "1"),
                },
            }
        }
    }


def tool_data(result: Any) -> Any:
    return to_plain(getattr(result, "data", result))


def to_plain(value: Any) -> Any:
    if value is None or isinstance(value, str | int | float | bool):
        return value
    if isinstance(value, datetime):
        return value.isoformat()
    if isinstance(value, list | tuple):
        return [to_plain(item) for item in value]
    if isinstance(value, dict):
        return {key: to_plain(item) for key, item in value.items()}

    attrs: dict[str, Any] = {}
    for name in dir(value):
        if name.startswith("_"):
            continue
        item = getattr(value, name)
        if callable(item):
            continue
        attrs[name] = to_plain(item)
    if attrs:
        return attrs
    return value


def enrich_trend_strategy(
    base_spec: dict[str, Any],
    *,
    symbol: str,
    interval_minutes: int,
    lookback: LookbackPair,
) -> dict[str, Any]:
    spec = dict(base_spec)
    spec["name"] = f"{symbol} {interval_minutes}m trend {lookback.fast}/{lookback.slow}"
    spec["family"] = "trend"
    spec["symbol"] = symbol
    spec["interval_minutes"] = interval_minutes
    spec["entry_rule"] = (
        f"signal_trend_up == 1 from ma_fast({lookback.fast}) > ma_slow({lookback.slow})"
    )
    spec["exit_rule"] = "signal_trend_up == 0 or max_holding_bars"
    spec["sizing_rule"] = "vol_targeted_research_only"
    spec["notes"] = (
        "Generated by trend_backtest runner. Backtest uses the deterministic "
        "lagged signal_trend_up feature; no live-trading authority is granted."
    )
    return spec


def summarize_experiment(
    *,
    rank: int,
    symbol: str,
    dataset_id: str,
    strategy_id: str,
    lookback: LookbackPair,
    backtest: dict[str, Any],
    walk_forward: dict[str, Any],
) -> ExperimentSummary:
    metrics = backtest["metrics"]
    folds = walk_forward.get("folds", [])
    walk_total = round(sum(float(fold["total_return_pct"]) for fold in folds), 4)
    positive_folds = sum(1 for fold in folds if float(fold["total_return_pct"]) > 0)
    positive_rate = round((positive_folds / max(len(folds), 1)) * 100, 4)
    total_return = float(metrics["total_return_pct"])
    max_drawdown = float(metrics["max_drawdown_pct"])
    score = round(total_return + walk_total + (positive_rate / 10) + max_drawdown, 4)
    return ExperimentSummary(
        rank=rank,
        symbol=symbol,
        dataset_id=dataset_id,
        strategy_id=strategy_id,
        lookback_fast=lookback.fast,
        lookback_slow=lookback.slow,
        backtest_status=str(backtest["status"]),
        walk_forward_status=str(walk_forward["status"]),
        total_return_pct=round(total_return, 4),
        benchmark_return_pct=float(metrics["benchmark_return_pct"]),
        max_drawdown_pct=round(max_drawdown, 4),
        trades=int(metrics["trades"]),
        walk_forward_total_return_pct=walk_total,
        positive_fold_rate_pct=positive_rate,
        score=score,
    )


def rank_summaries(summaries: list[ExperimentSummary]) -> list[ExperimentSummary]:
    ranked = sorted(
        summaries,
        key=lambda item: (
            item.walk_forward_status == "pass",
            item.score,
            item.total_return_pct,
            item.max_drawdown_pct,
        ),
        reverse=True,
    )
    return [summary.__class__(**{**asdict(summary), "rank": idx}) for idx, summary in enumerate(ranked, 1)]


async def run_symbol(
    client: Client,
    *,
    symbol: str,
    interval_minutes: int,
    max_rows: int,
    lookbacks: list[LookbackPair],
    fee_bps: float,
    slippage_bps: float,
    train_bars: int,
    test_bars: int,
) -> list[ExperimentSummary]:
    dataset_id = dataset_id_for(symbol, interval_minutes)
    await client.call_tool(
        "ingest_market_data",
        {
            "request": {
                "symbol": symbol,
                "interval_minutes": interval_minutes,
                "max_rows": max_rows,
            }
        },
    )
    profile = tool_data(await client.call_tool("profile_dataset", {"dataset_id": dataset_id}))
    if int(profile["rows"]) <= 0:
        raise RuntimeError(f"{dataset_id} has no rows after ingest")
    if int(profile["duplicate_rows"]) != 0:
        raise RuntimeError(f"{dataset_id} has duplicate rows: {profile['duplicate_rows']}")

    summaries: list[ExperimentSummary] = []
    for lookback in lookbacks:
        await client.call_tool(
            "build_feature_table",
            {
                "request": {
                    "dataset_id": dataset_id,
                    "lookback_fast": lookback.fast,
                    "lookback_slow": lookback.slow,
                }
            },
        )
        candidates = tool_data(
            await client.call_tool(
                "generate_strategy_candidates",
                {
                    "request": {
                        "symbol": symbol,
                        "interval_minutes": interval_minutes,
                        "family": "trend",
                        "count": 1,
                    }
                },
            )
        )
        spec = enrich_trend_strategy(
            candidates[0],
            symbol=symbol,
            interval_minutes=interval_minutes,
            lookback=lookback,
        )
        await client.call_tool("save_strategy", {"spec": spec})
        strategy_id = spec["strategy_id"]
        backtest = tool_data(
            await client.call_tool(
                "run_backtest",
                {
                    "request": {
                        "strategy_id": strategy_id,
                        "dataset_id": dataset_id,
                        "fee_bps": fee_bps,
                        "slippage_bps": slippage_bps,
                    }
                },
            )
        )
        walk_forward = tool_data(
            await client.call_tool(
                "run_walk_forward",
                {
                    "request": {
                        "strategy_id": strategy_id,
                        "dataset_id": dataset_id,
                        "train_bars": train_bars,
                        "test_bars": test_bars,
                    }
                },
            )
        )
        summaries.append(
            summarize_experiment(
                rank=0,
                symbol=symbol,
                dataset_id=dataset_id,
                strategy_id=strategy_id,
                lookback=lookback,
                backtest=backtest,
                walk_forward=walk_forward,
            )
        )
    return summaries


def write_report(repo_root: Path, summaries: list[ExperimentSummary]) -> Path:
    report_dir = repo_root / "artifacts" / "trend_experiments"
    report_dir.mkdir(parents=True, exist_ok=True)
    stamp = datetime.now(UTC).strftime("%Y%m%dT%H%M%SZ")
    path = report_dir / f"trend_experiment_{stamp}.json"
    payload = {
        "created_at": datetime.now(UTC).isoformat(),
        "summary": [asdict(summary) for summary in summaries],
        "live_trading": "not_used",
    }
    path.write_text(json.dumps(payload, indent=2), encoding="utf-8")
    return path


def print_table(summaries: list[ExperimentSummary], report_path: Path) -> None:
    print("rank symbol  lookback  bt_ret%  bench%  mdd%  wf_ret%  wf_pos%  trades  status")
    for item in summaries:
        print(
            f"{item.rank:>4} {item.symbol:<7} "
            f"{item.lookback_fast:>2}:{item.lookback_slow:<3} "
            f"{item.total_return_pct:>7.2f} "
            f"{item.benchmark_return_pct:>7.2f} "
            f"{item.max_drawdown_pct:>6.2f} "
            f"{item.walk_forward_total_return_pct:>7.2f} "
            f"{item.positive_fold_rate_pct:>7.2f} "
            f"{item.trades:>6} "
            f"{item.backtest_status}/{item.walk_forward_status}"
        )
    print(f"\nreport={report_path}")
    print("live_trading=not_used")


async def run(args: argparse.Namespace) -> list[ExperimentSummary]:
    repo_root = args.repo_root.resolve()
    config = mcp_stdio_config(repo_root, args.python)
    lookbacks = [parse_lookback(value) for value in args.lookbacks]
    async with Client(config, init_timeout=args.startup_timeout, timeout=args.tool_timeout) as client:
        all_summaries: list[ExperimentSummary] = []
        for symbol in args.symbols:
            all_summaries.extend(
                await run_symbol(
                    client,
                    symbol=symbol,
                    interval_minutes=args.interval_minutes,
                    max_rows=args.max_rows,
                    lookbacks=lookbacks,
                    fee_bps=args.fee_bps,
                    slippage_bps=args.slippage_bps,
                    train_bars=args.train_bars,
                    test_bars=args.test_bars,
                )
            )
    return rank_summaries(all_summaries)


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Run Kraken trend backtests through QuantResearchMCP.")
    parser.add_argument("--symbols", nargs="+", default=list(DEFAULT_SYMBOLS))
    parser.add_argument("--interval-minutes", type=int, default=60)
    parser.add_argument("--max-rows", type=int, default=500)
    parser.add_argument("--lookbacks", nargs="+", default=list(DEFAULT_LOOKBACKS))
    parser.add_argument("--fee-bps", type=float, default=10.0)
    parser.add_argument("--slippage-bps", type=float, default=5.0)
    parser.add_argument("--train-bars", type=int, default=200)
    parser.add_argument("--test-bars", type=int, default=50)
    parser.add_argument("--startup-timeout", type=float, default=90.0)
    parser.add_argument("--tool-timeout", type=float, default=120.0)
    parser.add_argument("--repo-root", type=Path, default=repo_root_from_module())
    parser.add_argument("--python", default=sys.executable)
    return parser


def main() -> None:
    args = build_parser().parse_args()
    summaries = asyncio.run(run(args))
    report_path = write_report(args.repo_root.resolve(), summaries)
    print_table(summaries, report_path)


if __name__ == "__main__":
    main()
